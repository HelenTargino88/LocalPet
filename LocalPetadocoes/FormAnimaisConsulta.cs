        private void btnAdiconar_Click(object sender, EventArgs e)
        {
            Animais animais = new Animais(txtNome.Text, Raca.ObterPorId(int.Parse(txtRaca.Text)), txtEspecie.Text, txtSexo.Text, txtPorte.Text, txtIdade.Text, txtDescricao.Text, txtEnfermidades.Text, txtMedicamentos.Text, txtVacinas.Text, txtComportamento.Text, txtImagem.Text, true);
            animais.Inserir();
            MessageBox.Show("Animal adicionado com sucesso!");
            txtNome.Clear();
            txtRaca.Clear();
            txtEspecie.Clear();
            txtSexo.Clear();
            txtPorte.Clear();
            txtIdade.Clear();
            txtDescricao.Clear();
            txtEnfermidades.Clear();
            txtMedicamentos.Clear();
            txtVacinas.Clear();
            txtComportamento.Clear();
            txtNome.Focus();
        }

        private void btnEditar_Click(object sender, EventArgs e)
